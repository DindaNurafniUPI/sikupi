2.Mata Kuliah Praktik Lapangan = 4 SKS<br>
3.Mata Kuliah Khusus Universitas = 2 SKS<br>
4.Mata Kuliah Khusus Fakultas (MKKF) = 6 SKS<br>
5.Mata Kuliah Keahlian Inti Program Studi (MKKIPS) = 102 SKS<br>
