<h2>Frequently Asked Questions (FAQ)</h2><br>
Temukan jawaban dari berbagai pertanyaan yang sering diajukan seputar Program Studi Sistem Informasi Kelautan (SIK)  UPI oleh calon pendaftar dan calon mahasiswa.
<br>
===========================================================================================
<br>
Q : Apa saja yang dipelajari di Sistem Informasi Kelautan?<br>

A : Belajar Sistem Informasi, Sistem Informasi Geografi, Kelautan, dan Perikanan.<br>

===========================================================================================<br>

Q : Apa anak SMK/IPS bisa masuk prodi Sistem Informasi Kelautan?<br>

A : Anak IPS tidak bisa, yang bisa masuk ke prodi Sistem Informasi Kelautan (SIK) adalah IPA dan SMK Jurusan tertentu. Jurusan SMK yang bisa masuk ke prodi SIK adalah Teknik Geomatika dan Geospasial, Teknik Komputer dan Informatika, Pelayaran Kapal Penangkap Ikan, Perikanan, dan Pengolahan Hasil Perikanan.

<br>===========================================================================================
<br>
Q : Apakah harus bisa berenang untuk masuk prodi Sistem Informasi Kelautan?<br>

A : Tidak harus, nanti akan ada mata kuliah renang  untuk melatih mahasiswa berenang.<br>

===========================================================================================<br>

Q : Bagaimana cara masuk prodi Sistem Informasi Kelautan?<br>

A : Mengikuti seleksi SNMPTN, SBMPTN, Mandiri UPI, dan Prestasi Mahasisiswa.<br>

===========================================================================================<br>

Q : Berapa passing grade masuk SIK?<br>

A : Universitas Pendidikan Indonesia selama ini tidak pernah merilis secara resmi passing grade minimal atau maksimal sebagai syarat kelulusan.

<br>===========================================================================================<br>

Q : Apa saja fasilitas di SIK?<br>

A : Laboratorium Komputer, Laboratorium sumber daya kelautan, Marine station Lontar, Lab Bahasa, perpustakaan dengan buku dan jurnal kelautan dan perikanan, ruang kuliah ber AC dan terkoneksi dengan Wifi.

<br>===========================================================================================<br>

Q : Apakah ada asrama untuk mahasiswa? Berapa kira-kira biayanya?<br>

A : Universitas Pendidikan Indonesia Kampus Daerah Serang memiliki asrama dengan kuota 10 orang untuk SIK. Biaya Rp. 2.000.000/tahun/mahasiswa dan dapat dicicil 2 x dalam setahun.
<br>
===========================================================================================<br>

Q : Bagaimana peluang kerja setelah lulus?<br>

A : Lulusan dapat bekerja di Kementerian/Lembaga/Badan pemerintahan seperti KKP, KEMENKO MARITIM, KLHK, LIPI, BMKG, LAPAN, BIG, BAKAMLA. Swasta seperti Konsultan Lingkungan, NGO, WWF, TNC, WCS, CSF, WRI, dan lain lain.
